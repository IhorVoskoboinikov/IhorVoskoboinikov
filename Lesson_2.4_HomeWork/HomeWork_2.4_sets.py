#Множества
#########################

a = {1, 2, 3, 4, 5}
b = {4, 5, 6, 7, 8}
c = {2, 4, 6, 8, 10 }

#######################
#Объединение списков
#######################

print(a | b |c)

###################
#Пересечение элементов
###################

print(a & c)

###################
#Разные элементы
###################

print(a ^ b)

